<h1>L'équipe d'administration :</h1>
</div>

<p>Le site est régulé par une équipe d'administrateurs responsable de la <b>bonne coordination</b> entre les élèves, et du <b>soutient informatique</b> en cas de problèmes.</p>
<p>Ces administrateurs ont le droit de <u>bannir ou approuver</u> les élèves sans rendre de comptes à quiconque d'autre que le <b>président de la CMC dentaire</b>, le <b>président de la CMC</b>, le <b>responsable Web de la CMC</b>, ainsi que <b>toute autre personne désignée</b> par les partis sus-mentionnés.<br/>
Ils peuvent également séléctionner parmis les utilisateurs du site de nouveaux administrateurs, à condition que l'utilisateur choisi soit d'accord avec cette décision.</p>
<p>Les administrateurs du site sont disponibles par l'onglet <b>"Contacter les admins"</b>, accessible à tout étudiant connécté.<br/>
Vous pouvez aussi leur envoyer un email ou les contacter <i>via</i> facebook, si ils utilisent ces outils.<br/>
Restez conscient que <i>la messagerie interne au site doit rester le moyen de communication privilégié</i> entre les étudiants et les administrateurs.</p>

